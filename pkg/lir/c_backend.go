func (g *cGen) cType(t *LType) string {
	if t == nil {
		return "void"
	}
	switch t.Kind {
	case LTyI8:
		return "int8_t"
	case LTyI16:
		return "int16_t"
	case LTyI32:
		return "int32_t"
	case LTyI64:
		return "int64_t"
	case LTyU8:
		return "uint8_t"
	case LTyU16:
		return "uint16_t"
	case LTyU32:
		return "uint32_t"
	case LTyU64:
		return "uint64_t"
	case LTyF32:
		return "float"
	case LTyF64:
		return "double"
	case LTyBool:
		return "bool"
	case LTyString:
		return "const char*"
	case LTyPlatformInt:
		return "int"
	case LTyPlatformUint:
		return "unsigned int"
	case LTySlice:
		return g.sliceTypeName(t.Elem)
	case LTyMap:
		return fmt.Sprintf("/* map<%s,%s> */void*", g.cType(t.Key), g.cType(t.Elem))
	case LTyStruct:
		return g.structName(t.Name, t.IsExported)
	case LTyClassHandle:
		return g.structName(t.Name, t.IsExported) + "*"
	case LTyOptional:
		return g.cType(t.Elem) // Simplified: just the inner type
	case LTyTaggedUnion:
		return g.structName(t.Name, t.IsExported)
	case LTyChannel:
		return "/* channel */ void*"
	case LTyFuncPtr:
		// Function pointer
		ret := g.cReturnType(t.Return)
		var params []string
		for _, p := range t.Params {
			params = append(params, g.cType(p))
		}
		if len(params) == 0 {
			return fmt.Sprintf("%s (*)(void)", ret)
		}
		return fmt.Sprintf("%s (*)(%s)", ret, strings.Join(params, ", "))
	case LTyUnit:
		return "void"
	case LTyError:
		return "int /* error */"
	case LTyErrorResult:
		return fmt.Sprintf("struct { %s value; int error; }", g.cType(t.Elem))
	case LTyTuple:
		var fields []string
		for i, f := range t.Fields {
			fields = append(fields, fmt.Sprintf("%s _%d", g.cType(f.Type), i))
		}
		return fmt.Sprintf("struct { %s }", strings.Join(fields, "; "))
	case LTyMutex:
		return "/* mutex */ int"
	case LTyTypeVar:
		return fmt.Sprintf("/* typevar %s */ void*", t.Name)
	case LTyAny:
		return "void*"
	default:
		return fmt.Sprintf("/* unknown type %d */", t.Kind)
	}
}

func (g *cGen) cFieldDecl(t *LType, name string) string {
	// Handle function pointer types which need the name embedded
	if t != nil && t.Kind == LTyFuncPtr {
		ret := g.cReturnType(t.Return)
		var params []string
		for _, p := range t.Params {
			params = append(params, g.cType(p))
		}
		if len(params) == 0 {
			return fmt.Sprintf("%s (*%s)(void)", ret, name)
		}
		return fmt.Sprintf("%s (*%s)(%s)", ret, name, strings.Join(params, ", "))
	}
	return g.cType(t) + " " + name
}

